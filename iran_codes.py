def search_by_phone_code(code):
    """جستجو بر اساس کد تلفن"""
    results = []
    if code in PHONE_CODES:
        for city in PHONE_CODES[code]:
            results.append({
                'name': city,
                'parent': 'استان مربوطه',
                'type': 'city',
                'code': code
            })
    return results

def search_by_code_or_name(query):
    """جستجو در کدها و نام‌ها"""
    results = []
    
    # جستجو در کدها
    for code, cities in PHONE_CODES.items():
        if query in code or query in cities:
            for city in cities:
                results.append({
                    'name': city,
                    'parent': 'استان مربوطه',
                    'type': 'city',
                    'code': code
                })
    
    return results